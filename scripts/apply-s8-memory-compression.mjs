let totalSaved = 0;
let totalApplied = 0;
let totalSkipped = 0;
const report = [];

for (const [relPath, rewrites] of Object.entries(REWRITES)) {
  const fullPath = resolve(ROOT, relPath);
  if (!existsSync(fullPath)) {
    report.push(`SKIP  ${relPath}  (not found)`);
    continue;
  }
  const before = readFileSync(fullPath, 'utf8');
  let after = before;
  let appliedHere = 0;
  let skippedHere = 0;
  for (const [from, to] of rewrites) {
    if (!after.includes(from)) {
      skippedHere += 1;
      totalSkipped += 1;
      continue;
    }
    after = after.replace(from, to);
    appliedHere += 1;
    totalApplied += 1;
  }
  if (after === before) {
    report.push(`SKIP  ${relPath}  (already compressed or no matches: ${skippedHere}/${rewrites.length} rewrites)`);
    continue;
  }
  writeFileSync(fullPath, after);
  const saved = before.length - after.length;
  totalSaved += saved;
  report.push(
    `OK    ${relPath}  (${appliedHere}/${rewrites.length} rewrites applied; saved ${saved} bytes; ${before.length} → ${after.length})`,
  );
}

process.stdout.write(report.join('\n') + '\n');
process.stdout.write(
  `\nSummary: ${totalApplied} rewrites applied, ${totalSkipped} skipped (idempotent), ${totalSaved} bytes saved.\n`,
);
process.stdout.write(
  'Inspect with: git diff CLAUDE.md ARCHITECTURE.md PRINCIPLES.md brain/INDEX.md\n',
);
